def count_neighbours(grid, row, col):
    neighbours = 0
    for x in range(max(0, row-1), row+2):
        for y in range(max(col-1, 0), col+2):
            try:
                if grid[x][y] == 1 and (x, y) != (row, col):neighbours += 1
            except IndexError:
                pass
    return neighbours


