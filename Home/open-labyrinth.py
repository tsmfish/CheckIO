def checkio(maze_map):
    MOVES = {"S": (1, 0), "N": (-1, 0), "W": (0, -1), "E": (0, 1)}
    map = maze_map[:]
    map[1][1] = 2
    wave, rank, noPath, hasPath= [(1,1)], 3, False, False
    while not noPath:
        nextWave, noPath = [], True
        for point in wave:
            for move in MOVES.values():
                if (point[0]+move[0], point[1]+move[1]) == (10,10):
                    hasPath = True
                    break
                if map[point[0]+move[0]][point[1]+move[1]] == 0:
                    map[point[0]+move[0]][point[1]+move[1]] = rank
                    noPath = False
                    nextWave.append((point[0]+move[0], point[1]+move[1]))
            if hasPath: break
        if hasPath: break
        rank += 1
        wave = nextWave
    route = ''
    if hasPath:
        rank -= 1
        position  = [10,10]
        while rank != 1:
            for move in MOVES.items():
                if map[position[0] - move[1][0]][position[1] - move[1][1]] == rank:
                    route+=move[0]
                    position = (position[0] - move[1][0],position[1] - move[1][1])
                    rank -= 1
                    break

    # for i in map: print(['%2d'% j for j in i])
    # print(route[::-1])
    return route[::-1] if route else None

